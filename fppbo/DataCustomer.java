    private void deleteCustomer(int id) {
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement("DELETE FROM customer WHERE id_customer = ?")) {
            ps.setInt(1, id);
            ps.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadCustomerData() {
        DefaultTableModel model = new DefaultTableModel(
        new Object [][]{}, new String[]{"ID", "Nama", "no. Telp.", "Alamat"}) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        try {
            Connection conn = DatabaseConnection.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM customer");
                while (rs.next()) {
                    model.addRow(new Object[] {
                        rs.getInt("id_customer"), rs.getString("nama_customer"), rs.getString("telp_customer"), rs.getString("alamat_customer")
                    });
                } 
                tableCustomer.setModel(model);
        } catch (SQLException e) {
            System.out.println("Gagal load data customer: " + e.getMessage());
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String nama = jTextField2.getText();
        String no_telp = jTextField3.getText();
        String alamat = jTextField4.getText();
        
        try{
            Connection conn = DatabaseConnection.getConnection();
            String sql = "INSERT INTO customer (id_customer, nama_customer, telp_customer, alamat_customer) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, id);
            ps.setString(2, nama);
            ps.setString(3, no_telp);
            ps.setString(4, alamat);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            loadCustomerData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

